const S_BOX_GENERATOR = {
    /**
     * 
     * @param {*} n 
     * @param {*} bits 
     * @returns 
     */
    build_multiple(n = 2) {
        const s_boxs = [];
        for (let i = 0; i < n; i++) {
            const s_box = this.build();
            const reverse_s_box = this.build_reverse(s_box);
            s_boxs.push({
                s_: s_box,
                _s: reverse_s_box
            });
        }
        return s_boxs;
    },
    /**
     * 
     */
    build() {
        // ---
        let s_box = new Array(256);
        // --- Riempio l'array
        for (let i = 0; i < 256; i++) {
            s_box[i] = i;
        }
        // --- Mescolo Casualmente
        for (let i = s_box.length - 1; i > 0; i--) {
            const j = Math.floor(Math.random() * (i + 1));
            [s_box[i], s_box[j]] = [s_box[j], s_box[i]];
        }
        // ---
        return s_box;
    },
    /**
     * 
     */
    build_reverse(s_box) {
        // ---
        let reverse_s_box = new Uint8Array(256);
        // ---
        for (let i = 0; i < 256; i++) {
            reverse_s_box[s_box[i]] = i;
        }
        // ---
        return reverse_s_box;
    }
}

const PERMUTE_GENERATOR = {
    build_multiple(n = 4, size = 4) {
        const Ps = [];
        for (let i = 0; i < n; i++) {
            const P = this.build(size);
            const reverse_P = this.build_reverse(P);
            Ps.push({
                p_: P,
                _p: reverse_P
            });
        }
        return Ps;
    },
    build(n) {
        const P = Array.from({ length: n }, (_, i) => i);
        // --- shuffle
        for (let i = P.length - 1; i > 0; i--) {
            const j = Math.floor(Math.random() * (i + 1));
            [P[i], P[j]] = [P[j], P[i]]; // Scambia sbox[i] e sbox[j]
        }
        // ---
        return P;
    },
    build_reverse(P) {
        const reverse_P = new Array(P.length);
        for (let i = 0; i < P.length; i++) {
            reverse_P[P[i]] = i;
        }
        return reverse_P;
    }
}

function MCD(a, b) {
    let r;
    r = a % b;
    while (r != 0) {
        a = b;
        b = r;
        r = a % b;
    }
    return b;
}

