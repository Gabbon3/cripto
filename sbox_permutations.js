const S_BOX_GENERATOR = {
    /**
     * 
     * @param {*} n 
     * @param {*} bits 
     * @returns 
     */
    build_multiple(n = 2, bits = 4) {
        const s_boxs = [];
        for (let i = 0; i < n; i++) {
            const s_box = this.build(bits);
            const reverse_s_box = this.build_reverse(s_box);
            s_boxs.push({
                s_: s_box,
                _s: reverse_s_box
            });
        }
        return s_boxs;
    },
    /**
     * 
     */
    build(bits = 4) {
        const s_box = [];
        for (let i = 0; i < (2 ** bits); i++) {
            const bit = i.toString(2);
            s_box.push('0'.repeat(bits - bit.length) + bit);
        }
        s_box.sort(() => Math.random() - 0.5);
        return s_box;
    },
    /**
     * 
     */
    build_reverse(s_box) {
        const bits = s_box[0].length;
        const reverse_s_box = new Array(s_box.length);
        for (let i = 0; i < s_box.length; i++) {
            const p = parseInt(s_box[i], 2);
            const bit = i.toString(2);
            reverse_s_box[p] = '0'.repeat(bits - bit.length) + bit;
        }
        return reverse_s_box;
    }
}

const PERMUTE_GENERATOR = {
    build_multiple(n = 4, bytes = 4) {
        const Ps = [];
        for (let i = 0; i < n; i++) {
            const P = this.build(bytes);
            const reverse_P = this.build_reverse(P);
            Ps.push({
                p_: P,
                _p: reverse_P
            });
        }
        return Ps;
    },
    build(bytes = 8) {
        const P = [];
        for (let i = 0; i < bytes * 8; i++) {
            P.push(i);
        }
        P.sort(() => Math.random() - 0.5);
        return P;
    },
    build_reverse(P) {
        const reverse_P = new Array(P.length);
        for (let i = 0; i < P.length; i++) {
            reverse_P[P[i]] = i;
        }
        return reverse_P;
    }
}

